// 從文字找行政區：先找明寫的「XX區」，其次獨特裸名，最後比對地標
export function extractDistrict(text) {
  for (const d of DISTRICTS) {
    if (text.includes(d)) return d;
    // 「台南市中西區」與「中西區」都能中；也接受「臺南東區」這種不含「市」的寫法
    const short = d.replace(/區$/, '');
    if (short.length >= 2 && new RegExp(`(台南|臺南)市?${short}`).test(text)) return d;
  }
  for (const name of SAFE_BARE_NAMES) {
    if (text.includes(name)) return `${name}區`;
  }
  for (const [mark, d] of Object.entries(LANDMARKS)) {
    if (text.includes(mark)) return d;
  }
  return '';
}

export function classifyCategory(text) {
  for (const { cat, keys } of CATEGORY_KEYWORDS) {
    if (keys.some(k => text.includes(k))) return cat;
  }
  return '';
}

// 依 id 給同區的標記一點固定偏移，地圖上才不會全部疊在同一點
export function geocode(district, id) {
  const base = DISTRICT_COORDS[district];
  if (!base) return { lat: '', lng: '' };
  let h = 0;
  for (const ch of String(id)) h = (h * 31 + ch.charCodeAt(0)) >>> 0;
  const jLat = ((h % 100) - 50) / 5000;        // ±0.01 度
  const jLng = (((h >> 8) % 100) - 50) / 5000;
  return { lat: +(base[0] + jLat).toFixed(5), lng: +(base[1] + jLng).toFixed(5) };
}
